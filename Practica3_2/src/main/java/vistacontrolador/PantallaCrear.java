    public static boolean verificarDobleContra(String contra1, String contra2) {
        return contra1.equals(contra2);
    }

    public static boolean verificarCorreo(String correo) {
        if (correo.matches("([a-z0-9]+(\\.?[a-z0-9])*)+@(([a-z]+)\\.([a-z]+))+")) {
            return true;
        }
        return false;
    }

    private boolean verificaciones() {
        String usuario = usuario1.getText();
        String contraseña = contra1.getText();
        String contraseña2 = confcontra.getText();

        if (usuario.equals("") || usuario == null) {
            JOptionPane.showMessageDialog(this, "El usuario no puede estar vacio.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (contraseña.equals("") || contraseña == null) {
            JOptionPane.showMessageDialog(this, "La contraseña no puede estar vacia.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (verificarDobleContra(contraseña, contraseña2)) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "La contraseña no coincide.", "Error", JOptionPane.ERROR_MESSAGE);

        }
        return false;
    }

