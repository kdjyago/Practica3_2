    private void crearbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearbtActionPerformed
        // TODO add your handling code here:
        try {
            Date selectedDate = fecha1.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String fecha2 = sdf.format(selectedDate);
            if (verificaciones()) {
                es.agregarUsuario(usuario1.getText(), contra1.getText(), nombre1.getText(), apellido1.getText(), apellido2.getText(), fecha2, correo.getText());
            }
        } catch (NullPointerException ex) {
            if (verificaciones()) {
                es.agregarUsuario(usuario1.getText(), contra1.getText(), nombre1.getText(), apellido1.getText(), apellido2.getText(), "sin fecha", correo.getText());
            }
        }
        this.setVisible(true);
    }//GEN-LAST:event_crearbtActionPerformed

