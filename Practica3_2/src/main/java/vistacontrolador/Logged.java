    private void guardarHistorialEnArchivo() {
        try {
            String fileName = "./historial.txt";
            BufferedWriter writer = new BufferedWriter(new FileWriter(fileName));
            historialLista = jTextField1.getText();
            writer.write(historialLista);
            writer.newLine();
            writer.close();
            JOptionPane.showMessageDialog(this, "Texto guardado en " + fileName, "Guardado", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el texto.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

